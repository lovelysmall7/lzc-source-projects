package com.lzc.demo.model;

import java.util.Date;

public class TaskWalkLength {
	private String id;
	private String workerId;
	private String type;
	private Integer distanse;
	private Date createTime;
	private Integer state;
	private Double distanseByMeter;
	
	public Double getDistanseByMeter() {
		return distanseByMeter;
	}
	public void setDistanseByMeter(Double distanseByMeter) {
		this.distanseByMeter = distanseByMeter;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getWorkerId() {
		return workerId;
	}
	public void setWorkerId(String workerId) {
		this.workerId = workerId;
	}
	public Integer getDistanse() {
		return distanse;
	}
	public void setDistanse(Integer distanse) {
		this.distanse = distanse;
	}
	public Date getCreateTime() {
		return createTime;
	}
	public void setCreateTime(Date createTime) {
		this.createTime = createTime;
	}
	public Integer getState() {
		return state;
	}
	public void setState(Integer state) {
		this.state = state;
	}
	
}
