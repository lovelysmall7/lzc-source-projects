package com.lzc.demo.model;

import java.util.Date;

/**
 * 零件与工位映射关系的实体类
 * @author lizc
 *
 */
public class ComponentStationMapping {
	private String id;
	private String componentName;
	private String stationName;
	private Date createTime;
	private String knockingPoint;
	
	public String getKnockingPoint() {
		return knockingPoint;
	}
	public void setKnockingPoint(String knockingPoint) {
		this.knockingPoint = knockingPoint;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getComponentName() {
		return componentName;
	}
	public void setComponentName(String componentName) {
		this.componentName = componentName;
	}
	public String getStationName() {
		return stationName;
	}
	public void setStationName(String stationName) {
		this.stationName = stationName;
	}
	public Date getCreateTime() {
		return createTime;
	}
	public void setCreateTime(Date createTime) {
		this.createTime = createTime;
	}
	
}
